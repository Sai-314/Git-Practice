public class ArithmeticOperations {
    public static void main(String[] args) {

        int i = 70 + 90;
        int j = i - 30;
        int k = j *20;
        double l = k * 20;

        System.out.println("i :"+i);
        System.out.println("j :"+j);
        System.out.println("k :"+k);
        System.out.println("l :"+l);
    }
}

